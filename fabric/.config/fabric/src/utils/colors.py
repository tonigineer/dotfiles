class Colors:
    """Class to define colors for terminal output"""

    # Reference: https://stackoverflow.com/questions/287871/print-in-terminal-with-colors-using-python
    HEADER = "\033[95m"
    INFO = "\033[94m"
    OKCYAN = "\033[96m"
    OKGREEN = "\033[92m"
    WARNING = "\033[93m"
    ERROR = "\033[91m"
    ENDC = "\033[0m"
    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    RESET = "\033[0m"
